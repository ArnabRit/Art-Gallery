// Event Listeners
document.querySelectorAll('.category-card').forEach(card => {
    card.addEventListener('click', () => {
        const category = card.dataset.category;
        showGallery(category);
    });
});

document.getElementById('image-modal').addEventListener('click', function(e) {
    if (e.target === this) {
        closeModal();
    }
});

// Functions
function showGallery(category) {
    const galleryData = window.galleryData[category];
    document.querySelector('.categories').style.display = 'none';
    const galleryView = document.getElementById('gallery-view');
    galleryView.style.display = 'block';
    
    document.getElementById('gallery-title').textContent = galleryData.title;
    
    const galleryGrid = document.getElementById('gallery-grid');
    galleryGrid.innerHTML = '';
    
    galleryData.images.forEach(item => {
        const galleryItem = document.createElement('div');
        galleryItem.className = 'gallery-item';
        
        if (item.type === 'video') {
            galleryItem.innerHTML = `
                <video src="${item.url}" muted loop>
                    Your browser does not support the video tag.
                </video>
                <div class="video-indicator">Video</div>
                <div class="gallery-item-overlay">
                    <h3>${item.title}</h3>
                </div>
            `;
            
            const video = galleryItem.querySelector('video');
            video.addEventListener('mouseover', () => video.play());
            video.addEventListener('mouseout', () => video.pause());
            galleryItem.addEventListener('click', () => showModal(item));
        } else {
            galleryItem.innerHTML = `
                <img src="${item.url}" alt="${item.title}">
                <div class="gallery-item-overlay">
                    <h3>${item.title}</h3>
                </div>
            `;
            galleryItem.addEventListener('click', () => showModal(item));
        }
        
        galleryGrid.appendChild(galleryItem);
    });
}

function showModal(item) {
    const modal = document.getElementById('image-modal');
    const modalContent = document.getElementById('modal-content');
    modalContent.innerHTML = '';

    if (item.type === 'video') {
        modalContent.innerHTML = `
            <video src="${item.url}" controls autoplay>
                Your browser does not support the video tag.
            </video>
        `;
    } else {
        modalContent.innerHTML = `<img src="${item.url}" alt="${item.title}">`;
    }

    modal.style.display = 'flex';
}

function showLanding() {
    document.querySelector('.categories').style.display = 'grid';
    document.getElementById('gallery-view').style.display = 'none';
}

function closeModal() {
    const modal = document.getElementById('image-modal');
    const modalContent = document.getElementById('modal-content');
    modal.style.display = 'none';
    modalContent.innerHTML = ''; // Clear content to stop video playback
}

